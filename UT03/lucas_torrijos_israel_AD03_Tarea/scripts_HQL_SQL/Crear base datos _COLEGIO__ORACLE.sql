-- TABLAS CENTROS CURSOS ALUMNOS ASIGNATURAS EVALUACIONES 
------------------------------------------------------------------
DROP TABLE EVALUACIONES CASCADE CONSTRAINTS;
DROP TABLE ALUMNOS CASCADE CONSTRAINTS;
DROP TABLE CENTROS CASCADE CONSTRAINTS;
DROP TABLE CURSOS CASCADE CONSTRAINTS;
DROP TABLE ASIGNATURAS CASCADE CONSTRAINTS;
DROP TABLE DEPARTAMENTOS CASCADE CONSTRAINTS;


CREATE TABLE CENTROS (
COD_CENTRO  NUMBER(5) PRIMARY KEY,
NOMBRE      VARCHAR2(25),
LOCALIDAD   VARCHAR2(20),
TLF         VARCHAR2(10),
NUM_ALUMNOS NUMBER(5),
NUM_CURSOS  NUMBER(5),
PRESUPUESTO NUMBER(7));


CREATE TABLE CURSOS(
COD_CURSO     NUMBER(5) PRIMARY KEY,
COD_CENTRO    NUMBER(5) REFERENCES CENTROS  (COD_CENTRO ),
DENOMINACION  VARCHAR2(25),
COSTE_MATRICULA  NUMBER(6),
NUM_ALUMNOS      NUMBER(5));


CREATE TABLE ALUMNOS(
NUM_ALUMNO  NUMBER(5) PRIMARY KEY,
COD_CURSO   NUMBER(5) REFERENCES CURSOS  (COD_CURSO ),
NOMBRE      VARCHAR2(25),
DIRECCION   VARCHAR2(25),
TLF         VARCHAR2(10),
NOTA_MEDIA  NUMBER(3,1));


CREATE TABLE DEPARTAMENTOS(
COD_DEPAR       NUMBER(5) PRIMARY KEY,
NOMBRE_DEPAR    VARCHAR2(25),
NUM_ASIG        NUMBER(5));


CREATE TABLE ASIGNATURAS(
COD_ASIG    NUMBER(5) PRIMARY KEY,
NOMBRE      VARCHAR2(25),
COD_DEPAR   NUMBER(5) REFERENCES DEPARTAMENTOS  (COD_DEPAR),
COD_CURSO   NUMBER(5) REFERENCES CURSOS  (COD_CURSO ));



CREATE TABLE EVALUACIONES(
COD_EVALUACION NUMBER(5),
NUM_ALUMNO     NUMBER(5) REFERENCES ALUMNOS  (NUM_ALUMNO),
COD_ASIG       NUMBER(5) REFERENCES ASIGNATURAS  (COD_ASIG),
NOTA           NUMBER(3,1),
CONSTRAINT PKEVA PRIMARY KEY (COD_EVALUACION, NUM_ALUMNO, COD_ASIG));


